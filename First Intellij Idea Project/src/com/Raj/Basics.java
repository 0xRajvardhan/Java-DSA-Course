package com.Raj;

public class Basics {
    public static void main(String[] args) {

        //if statement
        int a = 10;
        if (a == 10) {
            System.out.println("Yea bro");
        }

        //while loop
        int count = 1;
        while (count <= 5) {
            System.out.println(count);
            count++;
        }

        //for loop

        for(int number = 1; number != 5; number++) {
            System.out.println(number);
        }

    }
}
